                        <h4 class="text-danger">Explore and Experience the Beauty of Duars</h4>

                        <h2 class="text-warning">Duars Utsav-2005</h2>,<br>

                        The idea to have a Duars Utsav is not only to showcase the culture and people of the region but
                        also to use the event to generate revenue to be utilized for the development of the civic and
                        such other infrastructural facilities. A non-governmental initiative by the local people, Duars
                        Utsav – 2005 is going to be organized in Alipurduar for the first time from 27th January to 31st
                        January 2005. This also has the patronage of the local leadership and administration with the
                        local Ministers, Sabhadhipati, Jalpaiguri Zilla Parishad, Member of Parliament, Member of State
                        Legislative Assembly, Commissioner, Jalpaiguri Division, District Magistrate, Jalpaiguri,
                        Superintendent of Police, Jalpaiguri, Divisional Railway Manager, Northern Frontier Railway,
                        Alipurduar, Field Director, Buxa Tiger Reserve, Additional District Magistrate, Additional
                        Superintendent of Police, Alipurduar, and Chairman, Alipurduar Municipality having consented to
                        be in the Advisory Committee of the Utsav with the Sub-Divisional Officer, Alipurduar appointed
                        by the people to act as the President of the Utsav Committee. The administration here would be
                        more in the role of a coordinator and a facilitator than anything else.

                        As stated, the twin objective of the festival is to project and present the multi-splendoured
                        rich diversity of life and culture pervading the length and breadth of Dooars and to highlight
                        and harness the potentialities for the development of tourism and economy. The Dooars Utsav
                        beckons at all those who love nature, wish to explore and experience its virgin beauty and
                        bounty. Dooars’ innumerable streams, rivulets and rivers, its irresistible range of high hills
                        and lofty mountains with virgin trekking routes, magnificent verdant forests, rich wildlife,
                        lush green tea gardens and its pluralized society enriched by many tribes and indigenous people
                        are just too tempting to be missed out on.

                        The organization of such an Utsav with the theme ‘Explore and Experience the Beauty of Dooars’
                        had become inevitable more so because of its many unexploited and unharnessed potential for
                        development. Dooars Utsav is just an instrument, a vehicle to showcase and project the many
                        inherent strengths for development of the entire region rightly christened as Dooars, meaning
                        the portals to the Dragon Kingdom of neighbouring Bhutan.

                        The cultural diversities, which the Dooars can boast of, are matchless and peerless. Mecheni
                        dance, Chor-chunni, Bishahara, Dhamgan, Bhawaiya folk musical tradition and Rava dance are some
                        of the distinct elements of the local culture. The peaceful and harmonious co-existence of
                        Totos, Santal, Rava, Kotch, Oraon, Mech, Dukpas, Nepalese, Biharies, Marawaries and Bengalese is
                        one more vindication of the idea that is India. Inhabiting together in Dooars and making a
                        perfect mosaic, they truly make for a Mini India simultaneously being a testimony to the
                        celebration of life signifying unity in diversity.


